int h264_update_frame_header(GstBuffer * *buffer)
{
    int nalsize, size = GST_BUFFER_SIZE(*buffer);
    unsigned char *data = GST_BUFFER_DATA(*buffer);;
    unsigned char *p = data; 
    GstBuffer *hdrBuf;
    //g_print("h264_update_frame_header\n");
    if (p != NULL) {
        if (check_size_in_buffer(p, size)) {
            while ((p + 4) < (data + size)) {
                nalsize = (*p << 24) | (*(p + 1) << 16) | (*(p + 2) << 8) | (*(p + 3));
                *p = 0;
                *(p + 1) = 0;
                *(p + 2) = 0;
                *(p + 3) = 1;
                p += (nalsize + 4);
            }
            return 0;
        } else if (check_size_in_buffer3(p, size)) {
            while ((p + 3) < (data + size)) {
                nalsize = (*p << 16) | (*(p + 1) << 8) | (*(p + 2));
                *p = 0;
                *(p + 1) = 0;
                *(p + 2) = 1;
                p += (nalsize + 3);
            }
            return 0;
        } else if (check_size_in_buffer2(p, size)) {
            unsigned char *new_data;
            int new_len = 0;
            hdrBuf = gst_buffer_new_and_alloc(size + 2 * 1024);
            //bufout = GST_BUFFER_DATA(hdrBuf);
            new_data = GST_BUFFER_DATA(hdrBuf);;
            if (!new_data) {
                return -1;
            }

            while ((p + 2) < (data + size)) {
                nalsize = (*p << 8) | (*(p + 1));
                *(new_data + new_len) = 0;
                *(new_data + new_len + 1) = 0;
                *(new_data + new_len + 2) = 0;
                *(new_data + new_len + 3) = 1;
                memcpy(new_data + new_len + 4, p + 2, nalsize);
                p += (nalsize + 2);
                new_len += nalsize + 4;
            }
            gst_buffer_set_data(*buffer,new_data,size + 2 * 1024);
            GST_BUFFER_SIZE(*buffer)= new_len;
            if(hdrBuf)	
            gst_buffer_unref(hdrBuf);   
        }
    } else {
        g_print("[%s]invalid pointer!\n", __FUNCTION__);
        return -1;
    }
   return 0; 
}

