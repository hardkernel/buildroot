static int h264_add_header(GstAmlVout *Gstamlvout,GstBuffer **buffer)
{
    char nal_start_code[] = {0x0, 0x0, 0x0, 0x1};
    int nalsize;
    unsigned char* p;
    int tmpi;
    guint size = GST_BUFFER_SIZE(Gstamlvout->codec_data);	
    unsigned char* extradata = GST_BUFFER_DATA(Gstamlvout->codec_data);
    int header_len = 0;
    //char* buffer = pkt->hdr->data;
    GstBuffer *hdrBuf=NULL;
    unsigned char *bufout = NULL;  
    g_print("add 264 header in stream\n");
    p = extradata;
    if ((p[0] == 0 && p[1] == 0 && p[2] == 0 && p[3] == 1)||(p[0] == 0 && p[1] == 0 && p[2] == 1 ) && size < 1024) {
        g_print("add 264 header in stream before header len=%d", size);
         hdrBuf = gst_buffer_copy(Gstamlvout->codec_data);
	  *buffer = gst_buffer_merge(hdrBuf,*buffer);	 
        return 0;
    }
    if (size < 4) {
        return -1;
    }

    if (size < 10) {
        g_print("avcC too short\n");
        return -1;
    }

    if (*p != 1) {
        g_print(" Unkonwn avcC version %d\n", *p);
        return -1;
    }
    hdrBuf = gst_buffer_new_and_alloc(Gstamlvout->codec_data_len);
    bufout = GST_BUFFER_DATA(hdrBuf);
    int cnt = *(p + 5) & 0x1f; //number of sps
    // printf("number of sps :%d\n", cnt);
    p += 6;
    for (tmpi = 0; tmpi < cnt; tmpi++) {
        nalsize = (*p << 8) | (*(p + 1));
        memcpy(&(bufout[header_len]), nal_start_code, 4);
        header_len += 4;
        memcpy(&(bufout[header_len]), p + 2, nalsize);
        header_len += nalsize;
        p += (nalsize + 2);
    }

    cnt = *(p++); //Number of pps
    // printf("number of pps :%d\n", cnt);
    for (tmpi = 0; tmpi < cnt; tmpi++) {
        nalsize = (*p << 8) | (*(p + 1));
        memcpy(&(bufout[header_len]), nal_start_code, 4);
        header_len += 4;
        memcpy(&(bufout[header_len]), p + 2, nalsize);
        header_len += nalsize;
        p += (nalsize + 2);
    }
    if (header_len >= 1024) {
        g_print("header_len %d is larger than max length\n", header_len);
        return -1;
    }
    GST_BUFFER_SIZE(hdrBuf) = header_len;
    h264_update_frame_header(buffer);
    *buffer = gst_buffer_merge(hdrBuf,*buffer);
    if(hdrBuf)
        gst_buffer_unref(hdrBuf);
    return 0;
}

